import { builtinModules } from "node:module";
import { readFile, unlink } from "node:fs/promises";

const runtimePath = new URL("../vercel-runtime/server.mjs", import.meta.url);
const metafilePath = new URL("../vercel-runtime/server.meta.json", import.meta.url);
const source = await readFile(runtimePath, "utf8");
const metafile = JSON.parse(await readFile(metafilePath, "utf8"));
const runtimeOutput = Object.entries(metafile.outputs).find(([path]) => path.endsWith("vercel-runtime/server.mjs"));

if (!runtimeOutput) {
  throw new Error("Vercel runtime output is missing from the esbuild metafile.");
}

const importSpecifiers = new Set(runtimeOutput[1].imports.map(({ path }) => path));

const builtins = new Set([
  ...builtinModules,
  ...builtinModules.map((name) => `node:${name}`),
]);
// These are capability probes guarded by try/catch inside bundled libraries.
// They are optional accelerators/decoders and are not required to boot or serve APIs.
const optionalRuntimeImports = new Set([
  "bufferutil",
  "encoding",
  "supports-color",
  "utf-8-validate",
]);
const thirdPartyImports = [...importSpecifiers].filter((specifier) => (
  !specifier.startsWith(".")
  && !specifier.startsWith("/")
  && !builtins.has(specifier)
  && !optionalRuntimeImports.has(specifier)
));

if (thirdPartyImports.length > 0) {
  throw new Error(
    `Vercel runtime must be self-contained; external imports found: ${thirdPartyImports.join(", ")}`,
  );
}

if (!source.includes("Generated by npm run bundle:vercel")) {
  throw new Error("Vercel runtime generation banner is missing.");
}

await unlink(metafilePath);
console.log(`Verified self-contained Vercel runtime (${importSpecifiers.size} imports).`);
