import { builtinModules } from "node:module";
import { access, readFile, unlink } from "node:fs/promises";

const runtimePath = new URL("../vercel-runtime/server.mjs", import.meta.url);
const metafilePath = new URL("../vercel-runtime/server.meta.json", import.meta.url);
const protoAssetPath = new URL("../vercel-runtime/google-gax-protos/google/longrunning/operations.proto", import.meta.url);
const pdfWorkerPath = new URL("../vercel-runtime/pdf.worker.mjs", import.meta.url);
const nativeCanvasPath = new URL("../vercel-runtime/node_modules/@napi-rs/canvas/index.js", import.meta.url);
const nativeCanvasBinaryPath = new URL("../vercel-runtime/node_modules/@napi-rs/canvas-linux-x64-gnu/skia.linux-x64-gnu.node", import.meta.url);
const vercelConfigPath = new URL("../vercel.json", import.meta.url);
const source = await readFile(runtimePath, "utf8");
const metafile = JSON.parse(await readFile(metafilePath, "utf8"));
const vercelConfig = JSON.parse(await readFile(vercelConfigPath, "utf8"));
const runtimeOutput = Object.entries(metafile.outputs).find(([path]) => path.endsWith("vercel-runtime/server.mjs"));

if (!runtimeOutput) {
  throw new Error("Vercel runtime output is missing from the esbuild metafile.");
}

const importSpecifiers = new Set(runtimeOutput[1].imports.map(({ path }) => path));

const builtins = new Set([
  ...builtinModules,
  ...builtinModules.map((name) => `node:${name}`),
]);
// These are capability probes guarded by try/catch inside bundled libraries.
// They are optional accelerators/decoders and are not required to boot or serve APIs.
const optionalRuntimeImports = new Set([
  "bufferutil",
  "encoding",
  "supports-color",
  "utf-8-validate",
]);
const packagedRuntimeImports = new Set([
  "@napi-rs/canvas",
  "@napi-rs/canvas-linux-x64-gnu",
  "@napi-rs/canvas-linux-x64-musl",
]);
const thirdPartyImports = [...importSpecifiers].filter((specifier) => (
  !specifier.startsWith(".")
  && !specifier.startsWith("/")
  && !builtins.has(specifier)
  && !optionalRuntimeImports.has(specifier)
  && !packagedRuntimeImports.has(specifier)
));

if (thirdPartyImports.length > 0) {
  throw new Error(
    `Vercel runtime must be self-contained; external imports found: ${thirdPartyImports.join(", ")}`,
  );
}

if (!source.includes("Generated by npm run bundle:vercel")) {
  throw new Error("Vercel runtime generation banner is missing.");
}

if (!source.includes("__createNodeRequire(import.meta.url)")) {
  throw new Error("Vercel runtime is missing the ESM require bridge needed by CommonJS dependencies.");
}

if (!source.includes("google-gax-protos")) {
  throw new Error("Bundled google-gax protobuf lookup is missing from the Vercel runtime.");
}

await access(protoAssetPath);
await access(pdfWorkerPath);
await access(nativeCanvasPath);
await access(nativeCanvasBinaryPath);


if (source.includes("vercel-runtime/authoritative") || source.includes("authoritative/sft/SFT Bio Book.pdf")) {
  throw new Error("Large authoritative PDFs must not be embedded in the Vercel function runtime.");
}
const functionEntries = Object.entries(vercelConfig.functions || {});
const expectedFunctions = new Set(["api/index.ts"]);
if (functionEntries.length !== expectedFunctions.size || functionEntries.some(([name]) => !expectedFunctions.has(name))) {
  throw new Error("Vercel must package one self-contained Express API function.");
}

for (const [functionName, functionConfig] of functionEntries) {
  const includeFiles = Array.isArray(functionConfig.includeFiles)
    ? functionConfig.includeFiles
    : [functionConfig.includeFiles];
  if (!includeFiles.includes("vercel-runtime/**")) {
    throw new Error(`${functionName} must include every bundled runtime asset.`);
  }
  if (functionConfig.excludeFiles !== "node_modules/**") {
    throw new Error(`${functionName} must exclude node_modules after bundling.`);
  }
}

const apiRewrite = (vercelConfig.rewrites || []).find((rewrite) => rewrite.source === "/api/:path*");
if (!apiRewrite || apiRewrite.destination !== "/api?__path=:path*") {
  throw new Error("Nested API requests must be forwarded to the single Express function with the original path suffix.");
}
if ((vercelConfig.rewrites || []).some((rewrite) => rewrite.destination === "/api/index")) {
  throw new Error("The legacy /api/index rewrite discards the Express route and must not return.");
}

const bundledInputs = Object.keys(metafile.inputs || {});
if (!bundledInputs.some((input) => input.includes("node_modules/pdfjs-dist/legacy/build/pdf.mjs"))) {
  throw new Error("PDF.js escaped the Vercel runtime bundle; node_modules cannot be safely excluded.");
}

await unlink(metafilePath);
console.log(`Verified self-contained Vercel runtime (${importSpecifiers.size} imports).`);
