import { build } from "esbuild";
import { mkdir, writeFile } from "node:fs/promises";

const outputDirectory = new URL("../vercel-runtime/", import.meta.url);
const metafilePath = new URL("server.meta.json", outputDirectory);

await mkdir(outputDirectory, { recursive: true });

const result = await build({
  entryPoints: ["server.ts"],
  bundle: true,
  platform: "node",
  target: "node22",
  format: "esm",
  // google-gax resolves bundled protobuf files relative to its own CommonJS
  // __dirname. Keep that package native and make it a direct dependency so
  // Vercel's file tracer always includes its build/protos directory.
  external: ["google-gax"],
  define: {
    "process.env.NODE_ENV": '"production"',
  },
  minifyWhitespace: true,
  legalComments: "eof",
  metafile: true,
  banner: {
    js: [
      "// Generated by npm run bundle:vercel. Do not edit.",
      'import { createRequire as __createNodeRequire } from "node:module";',
      "const require = __createNodeRequire(import.meta.url);",
    ].join("\n"),
  },
  outfile: "vercel-runtime/server.mjs",
});

await writeFile(metafilePath, JSON.stringify(result.metafile));
console.log("Built the Vercel API runtime with a native ESM require bridge.");
