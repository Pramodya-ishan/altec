import { build } from "esbuild";
import { cp, mkdir, readFile, rm, writeFile } from "node:fs/promises";

const outputDirectory = new URL("../vercel-runtime/", import.meta.url);
const metafilePath = new URL("server.meta.json", outputDirectory);
const protoSourceDirectory = new URL("../node_modules/google-gax/build/protos/", import.meta.url);
const protoOutputDirectory = new URL("google-gax-protos/", outputDirectory);
const pdfWorkerSource = new URL("../node_modules/pdfjs-dist/legacy/build/pdf.worker.mjs", import.meta.url);
const pdfWorkerTarget = new URL("pdf.worker.mjs", outputDirectory);
const declarationTarget = new URL("server.d.mts", outputDirectory);

await mkdir(outputDirectory, { recursive: true });
await rm(protoOutputDirectory, { recursive: true, force: true });

const googleGaxProtoPathPlugin = {
  name: "google-gax-proto-path",
  setup(buildContext) {
    buildContext.onLoad(
      { filter: /node_modules[\\/]google-gax[\\/]build[\\/]src[\\/]grpc\.js$/ },
      async ({ path }) => {
        const originalSource = await readFile(path, "utf8");
        const originalExpression = "path.join(__dirname, '..', '..', 'build', 'protos')";
        // Do not put import.meta directly into google-gax's CommonJS module:
        // esbuild would then classify that module as ESM and its `exports`
        // references would fail at runtime.
        const bundledExpression = 'require("node:url").fileURLToPath(new URL("./google-gax-protos/", globalThis.__ALTEC_RUNTIME_URL__))';

        if (!originalSource.includes(originalExpression)) {
          throw new Error(
            "google-gax changed its protobuf lookup implementation; update the Vercel runtime bundler before deploying.",
          );
        }

        return {
          contents: originalSource.replace(originalExpression, bundledExpression),
          loader: "js",
        };
      },
    );
  },
};

const result = await build({
  entryPoints: ["server.ts"],
  bundle: true,
  platform: "node",
  target: "node22",
  format: "esm",
  plugins: [googleGaxProtoPathPlugin],
  define: {
    "process.env.NODE_ENV": '"production"',
  },
  minifyWhitespace: true,
  legalComments: "eof",
  metafile: true,
  banner: {
    js: [
      "// Generated by npm run bundle:vercel. Do not edit.",
      'import { createRequire as __createNodeRequire } from "node:module";',
      "const require = __createNodeRequire(import.meta.url);",
      "globalThis.__ALTEC_RUNTIME_URL__ = import.meta.url;",
    ].join("\n"),
  },
  outfile: "vercel-runtime/server.mjs",
});

await cp(protoSourceDirectory, protoOutputDirectory, { recursive: true });
try {
  await cp(pdfWorkerSource, pdfWorkerTarget);
} catch (error) {
  throw new Error(`PDF.js worker is missing and cannot be packaged: ${error instanceof Error ? error.message : String(error)}`);
}
await writeFile(metafilePath, JSON.stringify(result.metafile));
await writeFile(declarationTarget, 'declare const app: import("express").Express;\nexport default app;\n');
console.log("Built a self-contained Vercel API runtime with bundled Google protobuf and PDF.js worker assets.");
